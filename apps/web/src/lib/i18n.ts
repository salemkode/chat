export const translations = {
  en,
  ar,
} satisfies Record<Locale, TranslationMap>

export function isSupportedLocale(value: string): value is Locale {
  return value === 'en' || value === 'ar'
}

export function normalizeLocale(value?: string | null): Locale | null {
  if (!value) {
    return null
  }

  const normalized = value.trim().toLowerCase()
  if (isSupportedLocale(normalized)) {
    return normalized
  }

  const baseLocale = normalized.split('-')[0]
  return isSupportedLocale(baseLocale) ? baseLocale : null
}

export function getBrowserLocale(): Locale {
  if (typeof navigator === 'undefined') {
    return defaultLocale
  }

  const candidates = [...(navigator.languages ?? []), navigator.language]
  for (const candidate of candidates) {
    const normalized = normalizeLocale(candidate)
    if (normalized) {
      return normalized
    }
  }

  return defaultLocale
}

export function resolveLocale(preference: LocalePreference): Locale {
  return preference === 'auto' ? getBrowserLocale() : preference
}

export function normalizeLocalePreference(value?: string | null): LocalePreference {
  if (value === 'auto') {
    return value
  }

  return normalizeLocale(value) ?? defaultLocalePreference
}

export function getLocaleDirection(locale: Locale): Direction {
  return rtlLocales.has(locale) ? 'rtl' : 'ltr'
}

export function interpolate(message: string, values?: Record<string, string | number>): string {
  if (!values) {
    return message
  }

  return message.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`))
}

export function translate(
  locale: Locale,
  key: TranslationKey,
  values?: Record<string, string | number>,
): string {
  const template = translations[locale][key] ?? translations[defaultLocale][key]
  if (!template) {
    console.warn(`Missing translation for key "${key}" in locale "${locale}"`)
    return key
  }
  return interpolate(template, values)
}

export function getDocumentLocale(): Locale {
  if (typeof document === 'undefined') {
    return defaultLocale
  }

  return normalizeLocale(document.documentElement.lang) ?? defaultLocale
}
